/**
 * 地形モデルの設定定数
 * 
 * 地形モデルのスケール、位置、回転などの調整は、このファイルの定数を変更するだけで行えます。
 */

/**
 * 地形モデルのスケール設定
 * 
 * 地形モデルに適用されるスケール値です。
 * [x, y, z] の順で、各軸方向のスケール倍率を指定します。
 * 
 * 例: [10, 10, 10] は10倍に拡大
 * 例: [1, 1, 1] は元のサイズ
 */
export const TERRAIN_SCALE: [number, number, number] = [10, 10, 10];

/**
 * 水面モデルのスケール設定
 * 
 * 水面モデルに適用されるスケール値です。
 * 通常は地形と同じスケールを使用します。
 */
export const WATER_SCALE: [number, number, number] = [10, 10, 10];

/**
 * 地形モデルの位置オフセット
 * 
 * 地形モデルの中心を3D空間の原点 [0, 0, 0] に配置するための位置補正値です。
 * 
 * この値は、terrainScale適用後の地形モデルの実際の中心点の符号を反転させた値です。
 * モデルファイルが変更された場合や、スケールを変更した場合は、この値を再計算する必要があります。
 * 
 * 計算方法:
 * 1. devモードで3Dビューを開き、コンソールログで「地形のバウンディングボックス（terrainScale適用後、実際の値）」を確認
 * 2. 中心点の各座標の符号を反転させた値を設定
 * 
 * 例: 中心点が [-744.9999975831743, 177.19751206980436, 744.9999975831743] の場合
 *     → [744.9999975831743, -177.19751206980436, -744.9999975831743]
 */
export const TERRAIN_POSITION_OFFSET: [number, number, number] = [
  744.9999975831743,
  -177.19751206980436,
  -744.9999975831743,
];

/**
 * 地形モデルの実際のサイズ（terrainScale適用後）
 * 
 * 2Dマップ上に地形の範囲を表示するために使用されます。
 * この値は、terrainScale適用後の実際の地形モデルのサイズ（メートル）です。
 * 
 * モデルファイルが変更された場合や、スケールを変更した場合は、この値を再計算する必要があります。
 * 
 * 計算方法:
 * 1. devモードで3Dビューを開き、コンソールログで「地形のバウンディングボックス（terrainScale適用後、実際の値）」を確認
 * 2. サイズの x または z の値を設定（通常は x と z は同じ値）
 * 
 * 例: サイズが {x: 1490.0001525878906, y: 204.09059524536133, z: 1490.0001525878906} の場合
 *     → 1490.0001525878906
 */
export const TERRAIN_ACTUAL_SIZE = 1490.0001525878906;

/**
 * 地形モデルの全体スケール（オプション）
 * 
 * LakeModelコンポーネントの全体スケールです。
 * 通常は [1, 1, 1] のままにしておき、TERRAIN_SCALE で調整します。
 */
export const TERRAIN_OVERALL_SCALE: [number, number, number] = [1, 1, 1];

/**
 * 地形モデルの回転（オプション）
 * 
 * 地形モデルの回転角度（ラジアン）です。
 * 通常は [0, 0, 0] のままにしておきます。
 */
export const TERRAIN_ROTATION: [number, number, number] = [0, 0, 0];

